# With the given sets `s` and `t`, demonstrate how you can:
# - Create an intersection of both sets
s = {1, 2, 3, 4}
t = {3, 4, 5, 6}

print("s.intersection(t): ", s.intersection(t)) #solution one
print("t.intersection(s: ", t.intersection(s)) #solution two

# - Create a union of both sets
print("s.union(t): ", s.union(t)) #solution one
print("t.union(s): ", t.union(s)) #solution two

#each of these are sets
#the results from solution one are the same as the results from solution two